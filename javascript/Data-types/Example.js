// Number
let num = 100;
console.log("Number:", num, typeof num);

// String
let name = "Puneeth";
console.log("String:", name, typeof name);

// Boolean
let isActive = true;
console.log("Boolean:", isActive, typeof isActive);

// Undefined
let notAssigned;
console.log("Undefined:", notAssigned, typeof notAssigned);

// Null
let emptyValue = null;
console.log("Null:", emptyValue, typeof emptyValue);

// Symbol
let sym = Symbol("id");
console.log("Symbol:", sym, typeof sym);

// BigInt
let bigNumber = 1234567890123456789012345678901234567890n;
console.log("BigInt:", bigNumber, typeof bigNumber);

// Object
let user = {
  name: "Ram",
  age: 28,
};
console.log("Object:", user, typeof user);
